class SolanaScanner:
    DEXSCREENER_BASE = "https://api.dexscreener.com/latest/dex"
    BOOST_URL        = "https://api.dexscreener.com/token-boosts/latest/v1"

    async def _gmgn_cli(self, args: list[str]) -> Optional[dict]:
        """Call gmgn-cli via subprocess (bypasses Cloudflare via Node.js TLS)"""
        cmd = ["gmgn-cli"] + args
        try:
            proc = await asyncio.create_subprocess_exec(
                *cmd,
                stdout=asyncio.subprocess.PIPE,
                stderr=asyncio.subprocess.PIPE
            )
            stdout, stderr = await asyncio.wait_for(proc.communicate(), timeout=20)
            if proc.returncode != 0:
                err = stderr.decode().strip()
                logging.debug(f"gmgn-cli error: {err[:200]}")
                return None
            return json.loads(stdout.decode())
        except Exception as e:
            logging.debug(f"gmgn-cli call error: {e}")
            return None

    async def get_trending_tokens(self, session: aiohttp.ClientSession) -> list[TokenData]:
        """GMGN trending tokens (hot, volume, smart money)"""
        tokens = []
        data = await self._gmgn_cli(["market", "trending", "--chain", "sol", "--interval", "5m"])
        if not data:
            return await self._fallback_trending(session)
        if data.get("code") != 0:
            logging.warning(f"GMGN trending error: {data.get('msg', 'unknown')}")
            return await self._fallback_trending(session)
        rank = (data.get("data") or {}).get("rank") or []
        for item in rank[:25]:
            token = self._parse_gmgn_token(item)
            if token:
                tokens.append(token)
        return tokens

    async def get_new_tokens(self, session: aiohttp.ClientSession) -> list[TokenData]:
        """GMGN trenches — new pump.fun tokens"""
        tokens = []
        data = await self._gmgn_cli(["market", "trenches", "--chain", "sol", "--type", "new_creation"])
        if not data:
            return await self._fallback_new(session)
        # Trenches response: {"new_creation": [...], "completed": [...], ...}
        items = data.get("new_creation") or data.get("data", {}).get("list") or []
        if not items:
            # Try other keys
            for key in ("new_creation", "near_completion", "completed"):
                items = data.get(key) or []
                if items:
                    break
        for item in items[:20]:
            token = self._parse_gmgn_token(item)
            if token:
                tokens.append(token)
        return tokens
        return tokens

    async def get_token_detail(self, session: aiohttp.ClientSession, address: str) -> Optional[TokenData]:
        """GMGN token detail via gmgn-cli"""
        data = await self._gmgn_cli(["market", "kline", "--chain", "sol", "--address", address, "--resolution", "5m", "--limit", "1"])
        if not data:
            return None
        if data.get("code") != 0:
            return None
        # kline returns different format, try to get from trending instead
        return None

    def _parse_gmgn_token(self, item: dict) -> Optional[TokenData]:
        """Parse GMGN token data ke TokenData"""
        try:
            address = item.get("address", "")
            symbol = item.get("symbol", "???")
            name = item.get("name", "Unknown")
            price_usd = float(item.get("price", 0) or 0)
            # Trenches tokens may not have price yet — skip if zero
            if price_usd <= 0:
                # Try to get price from liquidity / supply
                liq_val = float(item.get("initial_liquidity", 0) or item.get("liquidity", 0) or 0)
                if liq_val > 0:
                    price_usd = liq_val / 1e9  # rough estimate
                else:
                    return None

            # Price changes
            pc = item.get("price_change_percent") or item.get("priceChange") or {}
            if isinstance(pc, (int, float)):
                ch5m = float(pc)
                ch1h = 0.0
                ch24h = 0.0
            else:
                ch5m = float(pc.get("m5", 0) or pc.get("price_change_percent5m", 0) or 0)
                ch1h = float(pc.get("h1", 0) or pc.get("price_change_percent1h", 0) or 0)
                ch24h = float(pc.get("h24", 0) or pc.get("price_change_percent24h", 0) or 0)

            # Volume
            vol = item.get("volume") or {}
            if isinstance(vol, (int, float)):
                vol_24h = float(vol)
                vol_5m = 0.0
            else:
                vol_24h = float(vol.get("h24", 0) or vol.get("volume24h", 0) or 0)
                vol_5m = float(vol.get("m5", 0) or vol.get("volume5m", 0) or 0)

            # Transactions - handle both trending and trenches format
            swaps = item.get("swaps") or item.get("txns") or {}
            if isinstance(swaps, (int, float)):
                total_swaps = int(swaps)
                buys_5m = total_swaps // 2
                sells_5m = total_swaps - buys_5m
            else:
                buys_5m = int(swaps.get("buys", 0) or swaps.get("buy", 0) or 0)
                sells_5m = int(swaps.get("sells", 0) or swaps.get("sell", 0) or 0)
                total_swaps = buys_5m + sells_5m
            # Trenches format: buys_24h, sells_24h
            if buys_5m == 0 and sells_5m == 0:
                buys_5m = int(item.get("buys_24h", 0) or 0)
                sells_5m = int(item.get("sells_24h", 0) or 0)
                total_swaps = buys_5m + sells_5m

            # Age - handle trenches format
            created_at = item.get("open_timestamp", 0) or item.get("creation_timestamp", 0) or item.get("created_timestamp", 0) or 0
            if created_at > 1e12:
                created_at = created_at / 1000
            age_minutes = max(0, (time.time() - created_at) / 60) if created_at > 0 else 0

            # Buy/sell ratio
            ratio = buys_5m / sells_5m if sells_5m > 0 else (999.0 if buys_5m > 0 else 0.0)

            # GMGN-specific
            rug_ratio = float(item.get("rug_ratio", 0) or 0)
            smart_degen = int(item.get("smart_degen_count", 0) or item.get("bot_degen_count", 0) or 0)
            renowned = int(item.get("renowned_count", 0) or 0)
            renounced_mint = bool(item.get("renounced_mint", 0))
            renounced_freeze = bool(item.get("renounced_freeze_account", 0))
            is_wash = bool(item.get("is_wash_trading", False))
            hot_level = int(item.get("hot_level", 0) or 0)
            creator_status = item.get("creator_token_status", "") or ""
            is_honeypot = bool(item.get("is_honeypot", False))
            top10_rate = float(item.get("top_10_holder_rate", 0) or item.get("creator_balance_rate", 0) or 0)
            dev_burn_ratio = float(item.get("dev_token_burn_ratio", 0) or 0)
            cto = bool(item.get("cto_flag", 0))

            return TokenData(
                address=address,
                symbol=symbol,
                name=name,
                price_usd=price_usd,
                price_sol=float(item.get("price_native", 0) or 0),
                liquidity_usd=float(item.get("liquidity", 0) or 0),
                volume_24h=vol_24h,
                volume_5m=vol_5m,
                price_change_5m=ch5m,
                price_change_1h=ch1h,
                price_change_24h=ch24h,
                market_cap=float(item.get("market_cap", 0) or item.get("marketCap", 0) or 0),
                holder_count=int(item.get("holder_count", 0) or 0),
                tx_count_5m=total_swaps,
                buy_count_5m=buys_5m,
                sell_count_5m=sells_5m,
                buy_sell_ratio=ratio,
                age_minutes=age_minutes,
                dex=item.get("dex", "") or item.get("exchange", "") or "unknown",
                pair_address=item.get("pair_address", "") or "",
                rug_ratio=rug_ratio,
                smart_degen_count=smart_degen,
                renowned_count=renowned,
                renounced_mint=renounced_mint,
                renounced_freeze=renounced_freeze,
                is_wash_trading=is_wash,
                hot_level=hot_level,
                creator_token_status=creator_status,
                is_honeypot=is_honeypot,
                top_10_holder_rate=top10_rate,
                dev_token_burn_ratio=dev_burn_ratio,
                cto_flag=cto,
            )
        except Exception as e:
            logging.debug(f"GMGN parse error: {e}")
            return None

    async def _fallback_trending(self, session: aiohttp.ClientSession) -> list[TokenData]:
        """Fallback: DexScreener token-boosts"""
        tokens = []
        try:
            async with session.get(self.BOOST_URL, timeout=aiohttp.ClientTimeout(total=10)) as r:
                if r.status != 200:
                    return tokens
                boosts = await r.json()
            if not isinstance(boosts, list):
                return tokens
            for boost in boosts[:15]:
                addr = boost.get("tokenAddress", "")
                if not addr:
                    continue
                try:
                    async with session.get(
                        f"{self.DEXSCREENER_BASE}/tokens/{addr}",
                        timeout=aiohttp.ClientTimeout(total=8)
                    ) as r:
                        if r.status != 200:
                            continue
                        data = await r.json()
                        pairs = data.get("pairs") or []
                        if not pairs:
                            continue
                        best = max(pairs, key=lambda x: float(x.get("liquidity", {}).get("usd", 0) or 0))
                        token = self._parse_dex_pair(best)
                        if token:
                            tokens.append(token)
                except Exception:
                    continue
                await asyncio.sleep(0.3)
        except Exception as e:
            logging.warning(f"Fallback trending error: {e}")
        return tokens

    async def _fallback_new(self, session: aiohttp.ClientSession) -> list[TokenData]:
        """Fallback: DexScreener search"""
        tokens = []
        try:
            url = f"{self.DEXSCREENER_BASE}/search?q=pump+fun+solana"
            async with session.get(url, timeout=aiohttp.ClientTimeout(total=12)) as r:
                if r.status != 200:
                    return tokens
                data = await r.json()
                pairs = data.get("pairs") or []
            sol_pairs = [p for p in pairs if p.get("chainId") == "solana"]
            for p in sol_pairs[:20]:
                token = self._parse_dex_pair(p)
                if token:
                    tokens.append(token)
        except Exception as e:
            logging.warning(f"Fallback new tokens error: {e}")
        return tokens

    def _parse_dex_pair(self, p: dict) -> Optional[TokenData]:
        """Parse DexScreener pair ke TokenData (fallback)"""
        try:
            base = p.get("baseToken", {})
            price_usd = float(p.get("priceUsd", 0) or 0)
            if price_usd <= 0:
                return None
            liq = p.get("liquidity", {})
            vol = p.get("volume", {})
            pc = p.get("priceChange", {})
            txns_5m = p.get("txns", {}).get("m5", {})
            info = p.get("info", {})
            created_at = p.get("pairCreatedAt", 0)
            age_minutes = (time.time() * 1000 - (created_at or time.time() * 1000)) / 60000
            holders = 0
            for ext in (info.get("extensions") or []):
                if isinstance(ext, dict) and ext.get("type") == "holders":
                    holders = int(ext.get("value", 0))
            buy5 = int(txns_5m.get("buys", 0) or 0)
            sell5 = int(txns_5m.get("sells", 0) or 0)
            ratio = buy5 / sell5 if sell5 > 0 else (999.0 if buy5 > 0 else 0.0)
            return TokenData(
                address=base.get("address", ""),
                symbol=base.get("symbol", "???"),
                name=base.get("name", "Unknown"),
                price_usd=price_usd,
                price_sol=float(p.get("priceNative", 0) or 0),
                liquidity_usd=float(liq.get("usd", 0) or 0),
                volume_24h=float(vol.get("h24", 0) or 0),
                volume_5m=float(vol.get("m5", 0) or 0),
                price_change_5m=float(pc.get("m5", 0) or 0),
                price_change_1h=float(pc.get("h1", 0) or 0),
                price_change_24h=float(pc.get("h24", 0) or 0),
                market_cap=float(p.get("marketCap", 0) or 0),
                holder_count=holders,
                tx_count_5m=buy5 + sell5,
                buy_count_5m=buy5,
                sell_count_5m=sell5,
                buy_sell_ratio=ratio,
                age_minutes=max(0, age_minutes),
                dex=p.get("dexId", "unknown"),
                pair_address=p.get("pairAddress", ""),
            )
        except Exception:
            return None


