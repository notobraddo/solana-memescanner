@dataclass
class TokenData:
    """Raw data dari scanner"""
    address: str
    symbol: str
    name: str
    price_usd: float
    price_sol: float
    liquidity_usd: float
    volume_24h: float
    volume_5m: float
    price_change_5m: float
    price_change_1h: float
    price_change_24h: float
    market_cap: float
    holder_count: int
    tx_count_5m: int
    buy_count_5m: int
    sell_count_5m: int
    age_minutes: float
    dex: str
    pair_address: str
    buy_sell_ratio: float = 0.0
    # GMGN-specific fields
    rug_ratio: float = 0.0
    smart_degen_count: int = 0
    renowned_count: int = 0
    renounced_mint: bool = False
    renounced_freeze: bool = False
    is_wash_trading: bool = False
    hot_level: int = 0
    creator_token_status: str = ""
    is_honeypot: bool = False
    top_10_holder_rate: float = 0.0
    dev_token_burn_ratio: float = 0.0
    cto_flag: bool = False
    scanned_at: float = field(default_factory=time.time)

    def entry_score(self) -> int:
        """
        Hitung entry score 0-100 untuk token baru.
        Lebih longgar — fokus ke momentum dan buy pressure.
        GMGN data: rug_ratio, smart_degen, renounced, wash_trading, hot_level.
        """
        score = 0

        # Liquidity (0-15) — token baru biasanya kecil
        if self.liquidity_usd >= 20_000:
            score += 15
        elif self.liquidity_usd >= 10_000:
            score += 12
        elif self.liquidity_usd >= 5_000:
            score += 8
        elif self.liquidity_usd >= 1_000:
            score += 4

        # Holders (0-10) — token baru biasanya sedikit
        if self.holder_count >= 100:
            score += 10
        elif self.holder_count >= 50:
            score += 7
        elif self.holder_count >= 20:
            score += 5
        elif self.holder_count >= 5:
            score += 2

        # Momentum 5m (0-20) — yang penting ada gerak
        if 2 <= self.price_change_5m <= 30:
            score += 20
        elif 1 <= self.price_change_5m <= 50:
            score += 15
        elif 0.5 <= self.price_change_5m <= 1:
            score += 10
        elif -5 <= self.price_change_5m < 0.5:
            score += 5

        # Buy/sell ratio (0-10) — buy > sell = bagus
        ratio = self.buy_count_5m / self.sell_count_5m if self.sell_count_5m > 0 else (self.buy_count_5m if self.buy_count_5m > 0 else 0)
        if ratio >= 2.0:
            score += 10
        elif ratio >= 1.5:
            score += 7
        elif ratio >= 1.0:
            score += 5
        elif ratio > 0:
            score += 2

        # Volume 24h (0-10) — ada volume = ada minat
        if self.volume_24h >= 50_000:
            score += 10
        elif self.volume_24h >= 10_000:
            score += 7
        elif self.volume_24h >= 1_000:
            score += 4

        # Age (0-10) — terlalu baru = risky, terlalu lama = gak ada momentum
        if 5 <= self.age_minutes <= 120:
            score += 10
        elif 1 <= self.age_minutes <= 360:
            score += 7
        elif self.age_minutes > 360:
            score += 3

        # GMGN: Smart money (0-10) — smart degen & renowned holders
        if self.smart_degen_count >= 5:
            score += 7
        elif self.smart_degen_count >= 2:
            score += 5
        elif self.smart_degen_count >= 1:
            score += 3
        if self.renowned_count >= 3:
            score += 3
        elif self.renowned_count >= 1:
            score += 2

        # GMGN: Safety (0-10) — renounced, no wash, low rug
        if self.renounced_mint and self.renounced_freeze:
            score += 4
        elif self.renounced_mint or self.renounced_freeze:
            score += 2
        if not self.is_wash_trading:
            score += 3
        if self.rug_ratio <= 0.1:
            score += 3
        elif self.rug_ratio <= 0.3:
            score += 1

        # GMGN: Hot level (0-5)
        if self.hot_level >= 3:
            score += 5
        elif self.hot_level >= 2:
            score += 3
        elif self.hot_level >= 1:
            score += 1

        # Penalty: creator masih hold (sell pressure risk)
        if self.creator_token_status == "creator_hold":
            score -= 5
        elif self.creator_token_status == "creator_close":
            score += 2

        # Penalty: top 10 holder rate tinggi = concentrated
        if self.top_10_holder_rate >= 0.5:
            score -= 5
        elif self.top_10_holder_rate >= 0.3:
            score -= 2

        # Penalty: honeypot
        if self.is_honeypot:
            score -= 20

        return max(0, min(score, 100))


@dataclass
class Trade:
    """Satu posisi trading"""
    id: str
    token_address: str
    token_symbol: str
    entry_type: str
    entry_price: float
    entry_price_sol: float
    tp_price: float
    sl_price: float
    size_sol: float
    status: str
    opened_at: float
    entry_score: int = 0
    closed_at: Optional[float] = None
    exit_price: Optional[float] = None
    pnl_sol: Optional[float] = None
    pnl_pct: Optional[float] = None
    ai_reasoning: str = ""
    current_price: float = 0.0
    last_updated: float = field(default_factory=time.time)
    is_paper: bool = True
    gmgn_data: Optional[dict] = None

    @property
    def is_active(self):
        return self.status in ("open", "pending_limit")

    @property
    def pnl_display(self):
        if self.pnl_sol is None:
            if self.status == "open" and self.current_price:
                pnl = (self.current_price - self.entry_price) / self.entry_price * self.size_sol
                return f"{pnl:+.4f} SOL ({(self.current_price/self.entry_price-1)*100:+.1f}%)"
            return "—"
        return f"{self.pnl_sol:+.4f} SOL ({self.pnl_pct:+.1f}%)"

    @property
    def duration_str(self) -> str:
        end = self.closed_at or time.time()
        mins = int((end - self.opened_at) / 60)
        if mins < 60:
            return f"{mins}m"
        return f"{mins//60}h {mins%60}m"


@dataclass
class BotState:
    """State lengkap bot"""
    balance_sol: float             = INITIAL_BALANCE_SOL
    initial_balance_sol: float     = INITIAL_BALANCE_SOL
    trades: list                   = field(default_factory=list)
    filters: FilterConfig          = field(default_factory=FilterConfig)
    consecutive_losses: int        = 0
    total_trades: int              = 0
    winning_trades: int            = 0
    losing_trades: int             = 0
    total_pnl_sol: float           = 0.0
    improve_count: int             = 0
    started_at: float              = field(default_factory=time.time)
    last_report_at: float          = field(default_factory=time.time)
    scanned_tokens_today: int      = 0
    skipped_tokens_today: int      = 0
    daily_loss_triggered: bool     = False
    paused_due_to_losses: bool     = False
    recent_losses: deque           = field(default_factory=lambda: deque(maxlen=10))
    trade_counter: int             = 0

    @property
    def win_rate(self) -> float:
        finished = self.winning_trades + self.losing_trades
        return self.winning_trades / finished if finished > 0 else 0.0

    @property
    def open_trades(self):
        return [t for t in self.trades if t.is_active]

    @property
    def finished_trades(self):
        return [t for t in self.trades if not t.is_active]

    @property
    def portfolio_change_pct(self) -> float:
        if self.initial_balance_sol == 0:
            return 0.0
        return (self.balance_sol - self.initial_balance_sol) / self.initial_balance_sol

    def can_open_trade(self) -> bool:
        if self.daily_loss_triggered:
            return False
        if self.paused_due_to_losses:
            return False
        if len(self.open_trades) >= MAX_OPEN_TRADES:
            return False
        if self.balance_sol < TRADE_SIZE_SOL:
            return False
        return True

    def check_daily_loss_limit(self) -> bool:
        """Return True jika daily loss limit terpicu"""
        if self.portfolio_change_pct <= -MAX_DAILY_LOSS_PCT:
            self.daily_loss_triggered = True
            return True
        return False

    def new_trade_id(self) -> str:
        self.trade_counter += 1
        return f"T{self.trade_counter:04d}"


