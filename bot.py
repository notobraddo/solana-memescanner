class NDA4Bot:
    def __init__(self):
        self.state    = BotState()
        self.scanner  = SolanaScanner()
        self.filters  = FilterEngine()
        self.ai       = AIAgent()
        self.notifier = TelegramNotifier()
        self.monitor  = TradeMonitor(self.state, self.notifier)
        self.seen_tokens: set = set()

    async def run(self):
        init_reference_files()
        logging.info("NDA4 Bot v2.1 starting...")

        connector = aiohttp.TCPConnector(limit=20)
        async with aiohttp.ClientSession(connector=connector) as session:
            # Kirim notif start
            await self.notifier.send(
                session,
                f"🚀 <b>NDA4 Bot v2.1 started</b>\n"
                f"Balance: {self.state.balance_sol:.4f} SOL | "
                f"Mode: {'PAPER' if PAPER_TRADE else 'LIVE'}\n"
                f"Max trades: {MAX_OPEN_TRADES} | Trade size: {TRADE_SIZE_SOL} SOL"
            )

            # Jalankan task paralel:
            # 1. Scan loop utama
            # 2. Monitor TP/SL/Limit
            # 3. Hourly report
            await asyncio.gather(
                self._scan_loop(session),
                self.monitor.monitor_loop(session),
                hourly_report_loop(session, self.state, self.notifier),
            )

    async def _scan_loop(self, session: aiohttp.ClientSession):
        logging.info("Scan loop started")
        while True:
            try:
                await self._scan_cycle(session)
            except Exception as e:
                logging.error(f"Scan cycle error: {e}")
            await asyncio.sleep(SCAN_INTERVAL_SEC)

    async def _scan_cycle(self, session: aiohttp.ClientSession):
        if not self.state.can_open_trade():
            logging.info(f"Cannot open trade: balance={self.state.balance_sol:.3f} open={len(self.state.active_trades)}")
            return

        # Scan tokens
        trending = await self.scanner.get_trending_tokens(session)
        new_tkns = await self.scanner.get_new_tokens(session)
        all_tokens = trending + new_tkns

        self.state.scanned_tokens_today += len(all_tokens)
        deduped = {t.address: t for t in all_tokens if t.address}.values()

        for token in deduped:
            if token.address in self.seen_tokens:
                continue
            if not self.state.can_open_trade():
                break

            # Hard filter
            ok, reason = self.filters.passes(token, self.state.filters)
            if not ok:
                self.state.skipped_tokens_today += 1
                logging.debug(f"SKIP {token.symbol}: {reason}")
                continue

            # Entry score check
            score = token.entry_score()
            if score < MIN_ENTRY_SCORE:
                self.state.skipped_tokens_today += 1
                logging.debug(f"SKIP {token.symbol}: score {score} < {MIN_ENTRY_SCORE}")
                continue

            # AI analysis
            ai_res = await self.ai.analyze_token(session, token, self.state)
            if not ai_res or ai_res.get("action") != "BUY":
                reason = ai_res.get("skip_reason", "AI: SKIP") if ai_res else "AI timeout"
                logging.info(f"AI SKIP {token.symbol}: {reason}")
                continue

            # Place trade
            await self._place_trade(session, token, ai_res)
            self.seen_tokens.add(token.address)

        # Auto-improve jika banyak losses
        if (self.state.consecutive_losses >= CONSECUTIVE_LOSS_TRIGGER
                and not self.state.paused_due_to_losses):
            await self._auto_improve(session)

    async def _place_trade(self, session: aiohttp.ClientSession, token: TokenData, ai_res: dict):
        entry_type  = ai_res.get("entry_type", "market")
        entry_price = float(ai_res.get("entry_price_usd", token.price_usd))
        tp_price    = float(ai_res.get("tp_price_usd", token.price_usd * self.state.filters.tp_multiplier))
        sl_price    = float(ai_res.get("sl_price_usd", token.price_usd * self.state.filters.sl_multiplier))
        confidence  = int(ai_res.get("confidence", 50))
        reasoning   = ai_res.get("reasoning", "")

        if entry_price <= 0 or tp_price <= entry_price or sl_price >= entry_price:
            logging.warning(f"Invalid prices for {token.symbol}, skipping")
            return

        trade = Trade(
            id                 = self.state.new_trade_id(),
            token_address      = token.address,
            token_symbol       = token.symbol,
            entry_type         = entry_type,
            entry_price        = entry_price,
            entry_price_sol    = token.price_sol,
            tp_price           = tp_price,
            sl_price           = sl_price,
            size_sol           = TRADE_SIZE_SOL,
            status             = "pending_limit" if entry_type == "limit" else "open",
            opened_at          = time.time(),
            entry_score        = token.entry_score(),
            ai_reasoning       = reasoning,
            current_price      = token.price_usd,
            is_paper           = PAPER_TRADE,
        )

        # Kurangi balance
        self.state.balance_sol -= TRADE_SIZE_SOL
        self.state.trades.append(trade)

        logging.info(
            f"[TRADE] {trade.status.upper()} {token.symbol} #{trade.id} "
            f"entry=${entry_price:.8f} TP=${tp_price:.8f} SL=${sl_price:.8f} "
            f"conf={confidence}"
        )

        # Notif entry
        await self.notifier.notify_entry(session, trade)

        # Journal
        append_to_journal(
            f"\n### [ENTRY] {token.symbol} #{trade.id} — {datetime.now().strftime('%Y-%m-%d %H:%M')}\n"
            f"- Type: {entry_type} | Entry: ${entry_price:.8f} | TP: ${tp_price:.8f} | SL: ${sl_price:.8f}\n"
            f"- Score: {trade.entry_score}/100 | Conf: {confidence}/100 | AI: {reasoning}\n"
        )

    async def _auto_improve(self, session: aiohttp.ClientSession):
        logging.info(f"Auto-improving filters (gen {self.state.filters.generation})...")
        result = await self.ai.improve_filters(session, self.state)
        if not result:
            return
        new_filters, notes = result
        self.state.filters = new_filters
        self.state.improve_count += 1
        self.state.consecutive_losses = 0

        msg = (
            f"🔧 <b>Filter Auto-Improved → Gen #{new_filters.generation}</b>\n"
            f"──────────────────\n"
            f"📝 {notes}\n"
            f"💧 Min Liq: ${new_filters.min_liquidity_usd:,.0f}\n"
            f"📊 Min Vol: ${new_filters.min_volume_24h_usd:,.0f}\n"
            f"🎯 TP: +{(new_filters.tp_multiplier-1)*100:.0f}%  "
            f"🛑 SL: -{(1-new_filters.sl_multiplier)*100:.0f}%"
        )
        await self.notifier.send(session, msg)
        append_to_strategy(
            f"\n## Generation {new_filters.generation} — {datetime.now().strftime('%Y-%m-%d %H:%M')}\n"
            f"{notes}\n{new_filters.to_prompt()}\n"
        )


