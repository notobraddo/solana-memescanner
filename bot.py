class TelegramNotifier:
    BASE_URL = f"https://api.telegram.org/bot{TELEGRAM_BOT_TOKEN}"
    MODE_TAG = "📄 PAPER" if PAPER_TRADE else "💸 LIVE"

    async def send(self, session: aiohttp.ClientSession, text: str, parse_mode: str = "HTML") -> bool:
        if not TELEGRAM_BOT_TOKEN or not TELEGRAM_CHAT_ID:
            logging.warning("Telegram token/chat_id not set, skipping notif.")
            return False
        try:
            async with session.post(
                f"{self.BASE_URL}/sendMessage",
                json={"chat_id": TELEGRAM_CHAT_ID, "text": text, "parse_mode": parse_mode},
                timeout=aiohttp.ClientTimeout(total=10),
            ) as r:
                ok = r.status == 200
                if not ok:
                    err = await r.text()
                    logging.warning(f"Telegram send failed {r.status}: {err[:100]}")
                return ok
        except Exception as e:
            logging.warning(f"Telegram error: {e}")
            return False

