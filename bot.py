#  TRADE MONITOR — cek TP/SL/Limit secara real-time
# ════════════════════════════════════════════════════════════════
class TradeMonitor:
    """
    Loop yang berjalan setiap 30 detik:
    1. Update harga current untuk semua active trade
    2. Cek limit order → jika price <= entry_price → set open + kirim notif
    3. Cek TP → jika price >= tp_price → close trade + kirim notif
    4. Cek SL → jika price <= sl_price → close trade + kirim notif
    """

    def __init__(self, state: BotState, notifier: TelegramNotifier):
        self.state    = state
        self.notifier = notifier
        self.fetcher  = PriceFetcher()

    async def _close_trade(self, trade: Trade, exit_price: float, reason: str):
        """Close trade dan update state"""
        trade.status     = reason  # "tp_hit" / "sl_hit"
        trade.exit_price = exit_price
        trade.closed_at  = time.time()
        trade.pnl_pct    = (exit_price / trade.entry_price - 1) * 100
        trade.pnl_sol    = trade.size_sol * (exit_price / trade.entry_price - 1)
        self.state.total_pnl_sol += trade.pnl_sol
        self.state.balance_sol   += trade.size_sol + trade.pnl_sol  # kembalikan modal + PnL
        self.state.total_trades  += 1

        if trade.pnl_sol >= 0:
            self.state.winning_trades   += 1
            self.state.consecutive_losses = 0
        else:
            self.state.losing_trades     += 1
            self.state.consecutive_losses += 1
            self.state.recent_losses.append(trade)

        if self.state.consecutive_losses >= MAX_CONSECUTIVE_LOSSES:
            self.state.paused_due_to_losses = True

        self.state.check_daily_loss_limit()

        # Journal entry
        pnl_tag = "WIN" if trade.pnl_sol >= 0 else "LOSS"
        append_to_journal(
            f"\n### [{pnl_tag}] {trade.token_symbol} #{trade.id} — {datetime.now().strftime('%Y-%m-%d %H:%M')}\n"
            f"- Entry: ${trade.entry_price:.8f}  Exit: ${exit_price:.8f}\n"
            f"- PnL: {trade.pnl_sol:+.4f} SOL ({trade.pnl_pct:+.1f}%)  Reason: {reason}\n"
            f"- Duration: {trade.duration_str}  Score: {trade.entry_score}/100\n"
        )

    async def monitor_loop(self, session: aiohttp.ClientSession):
        """Main monitoring loop — jalankan sebagai asyncio task"""
        logging.info("TradeMonitor started — checking every 30s")
        while True:
            try:
                await self._tick(session)
            except Exception as e:
                logging.error(f"TradeMonitor error: {e}")
            await asyncio.sleep(30)

    async def _tick(self, session: aiohttp.ClientSession):
        active = self.state.active_trades
        if not active:
            return

        for trade in active:
            price = await self.fetcher.get_price(session, trade.token_address)
            if not price:
                continue

            # Update current price
            trade.current_price = price
            trade.last_updated  = time.time()

            # ── 1. Limit order ke-hit ──────────────────────────
            if trade.status == "pending_limit":
                if price <= trade.entry_price:
                    trade.status = "open"
                    if not trade.notified_limit_hit:
                        trade.notified_limit_hit = True
                        await self.notifier.notify_limit_hit(session, trade)
                        logging.info(f"[LIMIT HIT] {trade.token_symbol} #{trade.id} @ ${price:.8f}")
                    continue  # Skip TP/SL check di cycle ini

            # ── 2. TP check ────────────────────────────────────
            if trade.status == "open" and price >= trade.tp_price:
                if not trade.notified_tp:
                    trade.notified_tp = True
                    await self._close_trade(trade, price, "tp_hit")
                    await self.notifier.notify_tp_hit(session, trade)
                    logging.info(f"[TP HIT] {trade.token_symbol} #{trade.id} @ ${price:.8f}  PnL={trade.pnl_sol:+.4f}")
                continue

            # ── 3. SL check ────────────────────────────────────
            if trade.status == "open" and price <= trade.sl_price:
                if not trade.notified_sl:
                    trade.notified_sl = True
                    await self._close_trade(trade, price, "sl_hit")
                    await self.notifier.notify_sl_hit(session, trade)
                    logging.info(f"[SL HIT] {trade.token_symbol} #{trade.id} @ ${price:.8f}  PnL={trade.pnl_sol:+.4f}")
                continue


