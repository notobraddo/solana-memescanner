    async def _call_api(self, session: aiohttp.ClientSession, prompt: str, max_tokens: int = 500, retries: int = 2) -> Optional[str]:
        payload = {
            "model": OPENROUTER_MODEL,
            "max_tokens": max_tokens,
            "messages": [{"role": "user", "content": prompt}]
        }
        for attempt in range(retries + 1):
            try:
                async with session.post(
                    OPENROUTER_BASE_URL,
                    headers=self.headers,
                    json=payload,
                    timeout=aiohttp.ClientTimeout(total=45)
                ) as r:
                    if r.status == 429:
                        wait = 10 * (attempt + 1)
                        logging.warning(f"OpenRouter rate limit, waiting {wait}s...")
                        await asyncio.sleep(wait)
                        continue
                    if r.status != 200:
                        err = await r.text()
                        logging.error(f"OpenRouter error {r.status}: {err[:200]}")
                        return None
                    data = await r.json()
                    try:
                        content = data["choices"][0]["message"]["content"]
                        if content is None:
                            logging.error("OpenRouter response content is None")
                            return None
                        return content.strip()
                    except (KeyError, IndexError) as e:
                        logging.error(f"OpenRouter response parse error: {e} | data: {str(data)[:300]}")
                        return None
            except asyncio.TimeoutError:
                logging.warning(f"OpenRouter timeout (attempt {attempt+1}/{retries+1})")
                if attempt < retries:
                    await asyncio.sleep(10 * (attempt + 1))
                    continue
                return None
            except Exception as e:
                logging.error(f"OpenRouter call error: {e}")
                return None
        return None

