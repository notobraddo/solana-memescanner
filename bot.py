    # ── LAPORAN 1 JAM (DIPERBAIKI — sync dengan semua trade) ─────
    async def send_hourly_report(self, session, state: BotState):
        """
        Laporan tiap 1 jam yang sync dengan:
        - Trade RUNNING (open)
        - Trade PENDING (limit order belum ke-hit)
        - Trade CLOSED hari ini
        - Jumlah SOL, PnL, WR
        """
        now = datetime.now()
        uptime_mins = int((time.time() - state.started_at) / 60)
        uptime_str  = f"{uptime_mins//60}j {uptime_mins%60}m" if uptime_mins >= 60 else f"{uptime_mins}m"

        open_trades    = state.open_trades
        pending_trades = state.pending_trades
        closed_trades  = state.finished_trades

        # ── Kalkulasi PnL ──────────────────────────────────────
        realized_pnl    = state.total_pnl_sol
        unrealized_pnl  = state.total_unrealized_pnl()
        total_pnl_all   = realized_pnl + unrealized_pnl
        portfolio_pct   = state.portfolio_change_pct * 100
        exposure_sol    = state.total_exposure_sol()

        # Win rate
        total_finished  = state.winning_trades + state.losing_trades
        wr_str          = f"{state.win_rate*100:.1f}%" if total_finished > 0 else "N/A"
        wr_emoji        = "🟢" if state.win_rate >= TARGET_WIN_RATE else ("🟡" if state.win_rate >= 0.4 else "🔴")

        # ── Header ─────────────────────────────────────────────
        lines = [
            f"📊 <b>LAPORAN 1 JAM — {now.strftime('%d/%m %H:%M')}</b> [{self.MODE_TAG}]",
            f"━━━━━━━━━━━━━━━━━━━━━",
            f"⏱ Uptime   : {uptime_str}",
            f"💼 Balance  : <b>{state.balance_sol:.4f} SOL</b>",
            f"📦 Exposure : {exposure_sol:.4f} SOL",
            f"",
        ]

        # ── Trade RUNNING ──────────────────────────────────────
        lines.append(f"🟢 <b>RUNNING ({len(open_trades)})</b>")
        if open_trades:
            for t in open_trades:
                upnl = t.unrealized_pnl()
                upct = t.unrealized_pct()
                price_str = f"${t.current_price:.8f}" if t.current_price else "—"
                emoji = "📈" if upnl >= 0 else "📉"
                lines.append(
                    f"  {emoji} <b>{t.token_symbol}</b> #{t.id}"
                    f"\n     Entry ${t.entry_price:.8f} → Now {price_str}"
                    f"\n     uPnL <code>{upnl:+.4f} SOL ({upct:+.1f}%)</code>"
                    f"  [{t.duration_str}]"
                )
        else:
            lines.append("  — tidak ada trade running")
        lines.append("")

        # ── Trade PENDING (limit belum hit) ────────────────────
        lines.append(f"🟡 <b>PENDING LIMIT ({len(pending_trades)})</b>")
        if pending_trades:
            for t in pending_trades:
                cur = t.current_price or 0
                gap_pct = ((t.entry_price - cur) / cur * 100) if cur > 0 else 0
                lines.append(
                    f"  ⏳ <b>{t.token_symbol}</b> #{t.id}"
                    f"\n     Limit ${t.entry_price:.8f}  (gap {gap_pct:+.1f}% dari harga sekarang)"
                    f"\n     TP ${t.tp_price:.8f}  |  SL ${t.sl_price:.8f}"
                )
        else:
            lines.append("  — tidak ada pending order")
        lines.append("")

        # ── Trade CLOSED ──────────────────────────────────────
        # Tampilkan 5 terakhir yang closed
