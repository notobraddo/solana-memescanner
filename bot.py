class JournalManager:
    def __init__(self):
        self._trade_batch = []

    def log_trade_open(self, trade: Trade, token: TokenData):
        entry = f"""
## Trade #{trade.id} — {datetime.now().strftime('%Y-%m-%d %H:%M')}

**Token**: {token.name} ({trade.token_symbol}) — `{trade.token_address}`
**Scanner**: DexScreener
**Entry Price**: ${trade.entry_price:.8f}
**Position Size**: {trade.size_sol} SOL ({"Paper" if trade.is_paper else "Live"})
**Entry Score**: {trade.entry_score}/100
**Entry Type**: {trade.entry_type.upper()}
**TP**: ${trade.tp_price:.8f} | **SL**: ${trade.sl_price:.8f}

### Entry Reasoning
- {trade.ai_reasoning}
- Liquidity: ${token.liquidity_usd:,.0f} | Volume 24h: ${token.volume_24h:,.0f}
- Buy/Sell 5m: {token.buy_count_5m}/{token.sell_count_5m} | Price change 5m: {token.price_change_5m:+.1f}%
- Age: {token.age_minutes:.0f} menit | Holders: {token.holder_count}
"""
        append_to_journal(entry)

    def log_trade_close(self, trade: Trade):
        won  = trade.pnl_sol and trade.pnl_sol > 0
        icon = "✅ WIN" if won else "❌ LOSS"
        outcome = f"""
### Outcome — {icon}
- **Exit Price**: ${trade.exit_price:.8f}
- **Exit Reason**: {trade.status.replace('_', ' ').upper()}
- **PnL**: {trade.pnl_sol:+.4f} SOL ({trade.pnl_pct:+.1f}%)
- **Duration**: {trade.duration_str}

### Learnings
- _[Bot akan update otomatis saat 10 trade selesai]_

---
"""
        append_to_journal(outcome)
        self._trade_batch.append(trade)

    def should_update_strategy(self) -> bool:
        return len(self._trade_batch) >= 10

    def flush_batch(self) -> list:
        batch = self._trade_batch[:]
        self._trade_batch.clear()
        return batch

    def log_strategy_iteration(self, state: BotState, notes: str):
        finished = state.finished_trades
        wins     = sum(1 for t in finished if t.pnl_sol and t.pnl_sol > 0)
        losses   = sum(1 for t in finished if t.pnl_sol and t.pnl_sol <= 0)
        wr       = wins / (wins + losses) * 100 if (wins + losses) > 0 else 0
        avg_win  = sum(t.pnl_pct for t in finished if t.pnl_pct and t.pnl_pct > 0) / max(wins, 1)
        avg_loss = sum(t.pnl_pct for t in finished if t.pnl_pct and t.pnl_pct <= 0) / max(losses, 1)

        entry = f"""
## Strategy Iteration #{state.improve_count} — {datetime.now().strftime('%Y-%m-%d %H:%M')}
Filter Generation: #{state.filters.generation}

### Performance
- Win Rate: {wr:.1f}%
- Avg Win: +{avg_win:.1f}% | Avg Loss: {avg_loss:.1f}%
- Net PnL: {state.total_pnl_sol:+.4f} SOL

### AI Pattern Notes
{notes}

---
"""
        append_to_strategy(entry)


