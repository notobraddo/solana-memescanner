class AIAgent:
    def __init__(self):
        self.headers = {
            "Content-Type": "application/json",
            "Authorization": f"Bearer {OPENROUTER_API_KEY}",
            "HTTP-Referer": "https://github.com/notobraddo/solana-memescanner",
            "X-Title": "NDA4 Solana Trading Bot v2.1",
        }

    async def _call_api(self, session: aiohttp.ClientSession, prompt: str, max_tokens: int = 500, retries: int = 2) -> Optional[str]:
        payload = {
            "model": OPENROUTER_MODEL,
            "max_tokens": max_tokens,
            "messages": [{"role": "user", "content": prompt}],
        }
        for attempt in range(retries + 1):
            try:
                async with session.post(
                    OPENROUTER_BASE_URL, headers=self.headers, json=payload,
                    timeout=aiohttp.ClientTimeout(total=45)
                ) as r:
                    if r.status == 429:
                        await asyncio.sleep(10 * (attempt + 1))
                        continue
                    if r.status != 200:
                        return None
                    data = await r.json()
                    content = data["choices"][0]["message"]["content"]
                    return content.strip() if content else None
            except asyncio.TimeoutError:
                if attempt < retries:
                    await asyncio.sleep(10 * (attempt + 1))
            except Exception as e:
                logging.error(f"OpenRouter error: {e}")
                return None
        return None

    async def analyze_token(self, session: aiohttp.ClientSession, token: TokenData, state: BotState) -> Optional[dict]:
        score = token.entry_score()
        prompt = f"""
Kamu adalah AI trading agent Solana on-chain yang disiplin.
{state.filters.to_prompt()}

TOKEN: {token.symbol} ({token.address[:8]}...)
Price: ${token.price_usd:.8f} | Liq: ${token.liquidity_usd:,.0f} | MCap: ${token.market_cap:,.0f}
Vol24h: ${token.volume_24h:,.0f} | Age: {token.age_minutes:.0f}m | Score: {score}/100
Change 5m/1h/24h: {token.price_change_5m:+.1f}% / {token.price_change_1h:+.1f}% / {token.price_change_24h:+.1f}%
Tx5m: {token.tx_count_5m} (B:{token.buy_count_5m} S:{token.sell_count_5m})
GMGN: rug={token.rug_ratio:.2f} smart_degen={token.smart_degen_count} hot={token.hot_level} honeypot={'Y' if token.is_honeypot else 'N'}
Renounced: {'✅' if token.renounced_mint else '❌'} | Wash: {'⚠️' if token.is_wash_trading else '✅'}

BOT: Balance={state.balance_sol:.3f} SOL | Open={len(state.open_trades)}/{MAX_OPEN_TRADES} | WR={state.win_rate*100:.1f}%

INSTRUKSI: Putuskan BUY atau SKIP. Jika BUY tentukan entry_type(market/limit), prices, confidence.
Jawab HANYA JSON (tanpa backtick):
{{"action":"BUY"/"SKIP","entry_type":"market"/"limit","entry_price_usd":float,"tp_price_usd":float,"sl_price_usd":float,"confidence":0-100,"reasoning":"max 2 kalimat","skip_reason":"jika SKIP"}}
"""
        text = await self._call_api(session, prompt, max_tokens=300)
        if not text:
            return None
        try:
            import re
            text = text.replace("```json", "").replace("```", "").strip()
            try:
                return json.loads(text)
            except json.JSONDecodeError:
                pass
            match = re.search(r'\{.*\}', text, re.DOTALL)
            return json.loads(match.group()) if match else None
        except Exception as e:
            logging.error(f"AI parse error: {e} | {text[:100]}")
            return None

    async def improve_filters(self, session: aiohttp.ClientSession, state: BotState):
        recent_losses_text = "\n".join([
            f"- {t.token_symbol}: PnL {t.pnl_pct:+.1f}%, Score {t.entry_score}/100"
            for t in list(state.recent_losses)[-5:] if t.pnl_sol is not None
        ]) or "—"
        current = state.filters
        prompt = f"""
Optimizer filter bot Solana. {state.consecutive_losses} loss berturut. WR={state.win_rate*100:.1f}%
Recent losses:\n{recent_losses_text}
{current.to_prompt()}
Analisis pola, adjust filter untuk WR 55%+.
Jawab HANYA JSON (tanpa backtick):
{{"min_liquidity_usd":float,"min_volume_24h_usd":float,"min_holder_count":int,"max_price_impact_pct":float,"min_price_change_5m":float,"max_price_change_5m":float,"min_tx_count_5m":int,"min_buy_sell_ratio":float,"min_market_cap_usd":float,"max_market_cap_usd":float,"bundle_threshold":float,"min_age_minutes":int,"max_age_hours":int,"tp_multiplier":float,"sl_multiplier":float,"improvement_notes":"max 3 kalimat"}}
"""
        text = await self._call_api(session, prompt, max_tokens=600)
        if not text:
            return self._fallback_improve(current)
        try:
            text = text.replace("```json", "").replace("```", "").strip()
            data = json.loads(text)
            notes = data.pop("improvement_notes", "Auto-improved")
            return FilterConfig(**data, generation=current.generation + 1), notes
        except Exception as e:
            logging.error(f"AI improve parse error: {e}")
            return self._fallback_improve(current)

    def _fallback_improve(self, current: FilterConfig):
        new_f = copy.copy(current)
        new_f.min_liquidity_usd    = min(current.min_liquidity_usd * 1.5, 100_000)
        new_f.min_volume_24h_usd   = min(current.min_volume_24h_usd * 1.5, 500_000)
        new_f.min_buy_sell_ratio   = min(current.min_buy_sell_ratio + 0.2, 3.0)
        new_f.generation           = current.generation + 1
        return new_f, "Fallback: naikkan liquidity, volume, buy/sell ratio"


